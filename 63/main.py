class Solution:
    def uniquePathsWithObstacles(self, obstacleGrid: list[list[int]]) -> int:
        costs = [[0]*len(obstacleGrid[0]) for i in range(len(obstacleGrid))]
        for i in range(len(obstacleGrid)):
            for j in range(len(obstacleGrid[0])):
                if obstacleGrid[i][j] == 1:
                    costs[i][j] = 0
                else:
                    if i == 0 and j == 0:
                        costs[i][j] = 1
                    else:
                        costs[i][j] = costs[i-1][j]+costs[i][j-1]
                        
        return costs[-1][-1]


